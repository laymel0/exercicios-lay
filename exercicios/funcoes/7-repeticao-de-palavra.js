/**
 * 7. Crie uma função que receba uma palavra e um número, e retorne essa palavra repetida N vezes. 
 */
function repeatWord( word , number) {
    console.log ("\n Quem é o menino mais lindo desse mundo? \n")
    for (let i = 0; i < number; i++ ){
        console.log ("♥" + word+ "♥");
    }
     
}
repeatWord("Lucas é o menino mais lindo do mundo", 10 );



 